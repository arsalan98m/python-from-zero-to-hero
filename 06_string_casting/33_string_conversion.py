# ********** String Conversion **********

# Converts numbers, lists, tuples, dictionaries, and booleans to strings.

# ✅ Example 1: Number → String
num_int: int = 100
num_str = str(num_int)
print(num_str, type(num_str))

# ✅ Example 2: Boolean → String
print(str(True))  # Output: "True"
print(str(False))  # Output: "False"

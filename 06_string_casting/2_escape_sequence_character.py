"""
Escape Sequence Character

In Python, escape sequence characters are used to represent special characters 
that have a specific meaning in a string.

These characters are denoted by a backslash `\` followed by a character.

"""

my_string = "Hello, \nWorld!"  # \n is used to insert a new line
print(my_string)

my_string2 = "Hello, \tWorld!"  # \t is used to insert a tab
print(my_string2)

my_string3 = "Hello, \bWorld!"  # \b is used to insert a backspace
print(my_string3)

my_string4 = "Hello, \\ World!"  # \ is used to insert a backslash
print(my_string4)
